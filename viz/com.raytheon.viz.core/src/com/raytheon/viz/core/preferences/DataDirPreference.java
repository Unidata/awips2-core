package com.raytheon.viz.core.preferences;

import org.eclipse.jface.preference.DirectoryFieldEditor;
import org.eclipse.jface.preference.FieldEditorPreferencePage;
import org.eclipse.ui.IWorkbench;
import org.eclipse.ui.IWorkbenchPreferencePage;

import com.raytheon.uf.viz.core.Activator;

/**
 * Server data directory preference page
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date         Ticket#    Engineer    Description
 * ------------ ---------- ----------- --------------------------
 * Jan 14, 2011            mschenke     Initial creation
 * 
 * </pre>
 * 
 * @author mschenke
 * @version 1.0
 */

public class DataDirPreference extends FieldEditorPreferencePage implements
        IWorkbenchPreferencePage {

    public DataDirPreference() {
        super(GRID);
        setPreferenceStore(Activator.getDefault().getPreferenceStore());
        setDescription("Specify mount point of the AWIPS Share directory.");
    }

    /*
     * (non-Javadoc)
     * 
     * @see
     * org.eclipse.jface.preference.FieldEditorPreferencePage#createFieldEditors
     * ()
     */
    @Override
    protected void createFieldEditors() {
        addField(new DirectoryFieldEditor(
                com.raytheon.uf.viz.core.preferences.PreferenceConstants.P_DATA_DIRECTORY,
                "AWIPS Share Dir", getFieldEditorParent()));
    }

    @Override
    public void init(IWorkbench workbench) {

    }

    @Override
    public boolean isValid() {
        return true;
    }
}
