package com.raytheon.uf.viz.localization.perspective.ui.custom.velocity;

import org.eclipse.jface.text.rules.IPredicateRule;
import org.eclipse.jface.text.rules.IToken;
import org.eclipse.jface.text.rules.MultiLineRule;
import org.eclipse.jface.text.rules.PatternRule;
import org.eclipse.jface.text.rules.RuleBasedPartitionScanner;
import org.eclipse.jface.text.rules.Token;

/**
 * Class for finding velocity comments
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date         Ticket#    Engineer    Description
 * ------------ ---------- ----------- --------------------------
 * Jul 12, 2011            mschenke     Initial creation
 * 
 * </pre>
 * 
 * @author mschenke
 */

public class VelocityCommentScanner extends RuleBasedPartitionScanner {

    public final static String VM_COMMENT = "__vm_comment";

    public VelocityCommentScanner() {
        IPredicateRule[] rules = new IPredicateRule[2];
        IToken comment = new Token(VM_COMMENT);

        rules[0] = new MultiLineRule("#*", "*#", comment);
        rules[1] = new PatternRule("##", null, comment, (char) 0, true, true);

        setPredicateRules(rules);
    }

}
