@DynamicSerialize
public class ParameterNode extends AbstractNode<LevelNode> {

    @DynamicSerializeElement
    private String parameterName;

    @DynamicSerializeElement
    private String parameterUnit;

    public String getParameterName() {
        return parameterName;
    }

    public void setParameterName(String parameterName) {
        this.parameterName = parameterName;
    }

    public String getParameterUnit() {
        return parameterUnit;
    }

    public void setParameterUnit(String parameterUnit) {
        this.parameterUnit = parameterUnit;
    }

    @Override
    public ParameterNode clone() {
        ParameterNode node = new ParameterNode();
        node.setValue(this.getValue());
        node.setParameterName(parameterName);
        node.setParameterUnit(parameterUnit);

        for (LevelNode child : this.getChildNodes().values()) {
            node.addChildNode((LevelNode) child.clone());
        }

        return node;
    }
}
